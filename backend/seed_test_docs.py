async def seed_test_documents():
    """Create test documents if they don't exist."""
    async with AsyncSessionLocal() as db:
        for doc_data in TEST_DOCS:
            # Check if doc with this title already exists
            result = await db.execute(
                select(Document).where(Document.title == doc_data["title"])
            )
            if result.scalar_one_or_none():
                print(f"✓ Document already exists: {doc_data['title']}")
                continue

            # Create new document
            doc = Document(
                title=doc_data["title"],
                filename=doc_data["filename"],
                file_path=None,
                content_type="text/plain",
                size=len(doc_data["extracted_text"]),
                status=doc_data["status"],
                classification=doc_data["classification"],
                category=doc_data["category"],
                jurisdiction=doc_data["jurisdiction"],
                language="kk",
                extracted_text=doc_data["extracted_text"],
                qdrant_id=str(uuid.uuid4()),
            )
            db.add(doc)
            await db.commit()
            print(f"✓ Created test document: {doc_data['title']}")


if __name__ == "__main__":
    asyncio.run(seed_test_documents())
